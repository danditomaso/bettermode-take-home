export function handleError(
  error: unknown,
  { request }: LoaderFunctionArgs | ActionFunctionArgs,
): void {
  if (request.signal.aborted) {
    return
  }
  if (error instanceof Error) {
    console.error(chalk.red(error.stack))
  } else {
    console.error(chalk.red(error))
  }
}